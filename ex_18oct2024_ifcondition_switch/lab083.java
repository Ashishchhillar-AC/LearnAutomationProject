package com.example.ex_18oct2024_ifcondition_switch;

public class lab083 {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
